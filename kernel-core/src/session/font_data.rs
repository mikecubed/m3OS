//! Phase 57 Track G.2 — bitmap font data.
//!
//! Source: public-domain IBM VGA BIOS 8×16 font, glyphs 0x20–0x7E
//! (printable ASCII). The data is identical to the table in
//! `kernel/src/fb/mod.rs`, copied here so the `term` userspace client
//! can render without depending on the kernel crate. The kernel
//! framebuffer console will switch to consuming this same table in a
//! follow-up cleanup; until then both copies are byte-for-byte
//! identical.
//!
//! Each glyph is 16 bytes — one byte per row, 16 rows. Bit 7 of each
//! byte is the leftmost pixel; an 8-pixel-wide cell fits exactly into
//! one byte per row. This matches the layout documented on
//! [`crate::session::font::Glyph`].
//!
//! 0x7F (DEL) is rendered as an empty cell (all background). The
//! [`Glyph`] for it is included in the table so [`BasicBitmapFont`]
//! covers the full ASCII printable range plus DEL.

