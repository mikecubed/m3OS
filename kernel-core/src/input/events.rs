    // ---- KeyEvent round-trip -------------------------------------------

    #[test]
    #[ignore = "A.0 stub: real codec impl lands in A.0 commit 2"]
    fn key_event_down_round_trips() {
        let ev = KeyEvent {
            timestamp_ms: 0x1234_5678_9abc_def0,
            keycode: 0x30,
            symbol: b'a' as u32,
            modifiers: ModifierState(MOD_SHIFT | MOD_CTRL),
            kind: KeyEventKind::Down,
        };
        let mut buf = [0u8; KEY_EVENT_WIRE_SIZE];
        let n = ev.encode(&mut buf).expect("encode");
        assert_eq!(n, KEY_EVENT_WIRE_SIZE);
        let (back, consumed) = KeyEvent::decode(&buf).expect("decode");
        assert_eq!(consumed, KEY_EVENT_WIRE_SIZE);
        assert_eq!(back, ev);
    }

    #[test]
    #[ignore = "A.0 stub: real codec impl lands in A.0 commit 2"]
    fn key_event_up_round_trips() {
        let ev = KeyEvent {
            timestamp_ms: 0,
            keycode: u32::MAX,
            symbol: 0,
            modifiers: ModifierState(MOD_ALL),
            kind: KeyEventKind::Up,
        };
        let mut buf = [0u8; KEY_EVENT_WIRE_SIZE];
        ev.encode(&mut buf).expect("encode");
        let (back, _) = KeyEvent::decode(&buf).expect("decode");
        assert_eq!(back, ev);
    }

    #[test]
    #[ignore = "A.0 stub: real codec impl lands in A.0 commit 2"]
    fn key_event_repeat_round_trips() {
        let ev = KeyEvent {
            timestamp_ms: 42,
            keycode: 0x0d,
            symbol: 0x0a,
            modifiers: ModifierState::empty(),
            kind: KeyEventKind::Repeat,
        };
        let mut buf = [0u8; KEY_EVENT_WIRE_SIZE];
        ev.encode(&mut buf).unwrap();
        assert_eq!(KeyEvent::decode(&buf).unwrap().0, ev);
    }

    #[test]
    #[ignore = "A.0 stub: real codec impl lands in A.0 commit 2"]
    fn key_event_short_buffer_returns_truncated() {
        let ev = KeyEvent {
            timestamp_ms: 0,
            keycode: 0,
            symbol: 0,
            modifiers: ModifierState::empty(),
            kind: KeyEventKind::Down,
        };
        let mut tiny = [0u8; KEY_EVENT_WIRE_SIZE - 1];
        assert_eq!(ev.encode(&mut tiny), Err(EventCodecError::Truncated));
        let tiny_in = [0u8; KEY_EVENT_WIRE_SIZE - 1];
        assert_eq!(KeyEvent::decode(&tiny_in), Err(EventCodecError::Truncated));
    }

    #[test]
    #[ignore = "A.0 stub: real codec impl lands in A.0 commit 2"]
    fn key_event_rejects_unknown_kind_tag() {
        let mut buf = [0u8; KEY_EVENT_WIRE_SIZE];
        buf[18] = 7;
        assert_eq!(
            KeyEvent::decode(&buf).unwrap_err(),
            EventCodecError::InvalidKeyKind
        );
    }

    #[test]
    #[ignore = "A.0 stub: real codec impl lands in A.0 commit 2"]
    fn key_event_rejects_unknown_modifier_bit() {
        let mut buf = [0u8; KEY_EVENT_WIRE_SIZE];
        let bad = MOD_ALL | (1 << 15);
        buf[16..18].copy_from_slice(&bad.to_le_bytes());
        assert_eq!(
            KeyEvent::decode(&buf).unwrap_err(),
            EventCodecError::InvalidModifier
        );
    }

